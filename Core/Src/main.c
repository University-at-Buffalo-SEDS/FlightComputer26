int fputc(int ch, FILE *f) {
  (void)f;

  static uint8_t last_was_cr = 0;
  uint8_t buf[2];
  uint16_t w = 0;

  if (!usb_cdc_ready()) {
    return ch;
  }

  if (ch == '\n') {
    if (!last_was_cr) {
      buf[w++] = '\r';
    }
    buf[w++] = '\n';
    last_was_cr = 0;
  } else {
    buf[w++] = (uint8_t)ch;
    last_was_cr = (ch == '\r');
  }

  if (w > 0) {
    cdc_write_raw(buf, w);
  }
  return ch;
}
